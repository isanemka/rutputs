  name: 'PrivacyPolicyComponent',
  meta: {
    title: 'Integritetspolicy – Rutputs',
    meta: {
      description: {
        name: 'description',
        content: 'Läs om hur Rutputs hanterar dina personuppgifter, cookies och webbanalys i enlighet med GDPR.'
      },
      ogTitle: {
        property: 'og:title',
        content: 'Integritetspolicy – Rutputs'
      },
      ogDescription: {
        property: 'og:description',
        content: 'Läs om hur Rutputs hanterar dina personuppgifter, cookies och webbanalys i enlighet med GDPR.'
      },
      ogImage: {
        property: 'og:image',
        content: 'https://www.rutputs.nu/og-image.png'
      },
      ogUrl: {
        property: 'og:url',
        content: 'https://www.rutputs.nu/integritetspolicy'
      }
    },
    link: {
      canonical: {
        rel: 'canonical',
        href: 'https://www.rutputs.nu/integritetspolicy'
      }
    }
  },
  setup() {
    const {
      consentStatus,
      acceptConsent,
      rejectConsent,
      triggerShowBanner,
    } = useConsent();

    const statusColor = computed(() => {
      switch (consentStatus.value) {
        case 'accepted':
          return 'positive';
        case 'rejected':
          return 'negative';
        default:
          return 'grey';
      }
    });

    const statusLabel = computed(() => {
      switch (consentStatus.value) {
        case 'accepted':
          return 'Accepterat';
        case 'rejected':
          return 'Avvisat';
        default:
          return 'Inget val gjort';
      }
    });

    const changeConsentLabel = computed(() => {
      if (consentStatus.value === 'pending') {
        return 'Öppna cookieinställningar';
      }
      return 'Ändra mitt val';
    });

    const handleConsentChange = () => {
      triggerShowBanner();
    };

    return {
      consentStatus,
      statusColor,
      statusLabel,
      changeConsentLabel,
      handleConsentChange,
    };
  },
});
</script>

